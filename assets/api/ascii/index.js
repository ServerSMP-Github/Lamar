function stringToAscii(message) {
    let result = '';

    for (let row = 0; row < 5; row++) {
        for (let char of message) {
            const charRepresentation = font[char] ? font[char][row] : '-'.repeat(6);

            result += charRepresentation + '  ';
        }
        result += '\n';
    }

    return result;
}

